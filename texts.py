def plural_ru(n: int, f1: str, f2: str, f5: str) -> str:
    """–†—É—Å—Å–∫–æ–µ —Å–∫–ª–æ–Ω–µ–Ω–∏–µ: 1 –º–∏–Ω—É—Ç–∞, 2 –º–∏–Ω—É—Ç—ã, 5 –º–∏–Ω—É—Ç."""
    n = abs(int(n))
    n10 = n % 10
    n100 = n % 100
    if n10 == 1 and n100 != 11:
        return f1
    if 2 <= n10 <= 4 and not (12 <= n100 <= 14):
        return f2
    return f5


