# ===== Нормализация фраз «Быстрый турнир» =====
# Делает «быстрый турнир», «БЫСТРЫЙ ТУРНИР», «быСтРыЙ ТуРнИр» и т.п. → «Быстрый турнир»
_BTW_PATTERN = re.compile(r"\bбыстрый\s+турнир\b", flags=re.IGNORECASE)


def _normalize_tourney_case(s: str) -> str:
    return _BTW_PATTERN.sub("Быстрый турнир", s)


def tournament_phrase_by_index(idx: int) -> tuple[str, int]:
    """
    Возвращает кортеж (фраза, следующий_индекс) по кругу из TOURNEY_TEMPLATES.
    Гарантируется, что «Быстрый турнир» будет с прописной буквы.
    """
    n = len(TOURNEY_TEMPLATES)
    if n == 0:
        return "Быстрый турнир через 5 минут!", 0
    i = idx % n
    phrase = _normalize_tourney_case(TOURNEY_TEMPLATES[i])
    return phrase, (i + 1) % n


